numero = str(input('Você é solteiro, casado, divorciado, disquitado ou viuvo? \n'))

if numero == 'solteiro':
    print('Você é Solteiro')
elif numero == 'casado':
    print('Você é Casado')
elif numero =='divorciado':
    print('Você é Divorciado')
elif numero == 'disquitado':
    print('Você é disquitado')
elif numero == 'viuvo':
    print('Você é viúvo')
else:
    print('A opção digitada não é válida')
